package ar.edu.utn.frba.sceu.truco;

public class Card 
{
	private int numero;
	private int palo;
	
	
	public Card(int numero, int palo) 
	{

		this.numero = numero;
		this.palo = palo;
	}
	
	public int getNumero() 
	{
		return numero;
	}
	public int getPalo() 
	{
		return palo;
	}
	
	

}
